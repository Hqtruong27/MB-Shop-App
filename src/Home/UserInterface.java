    private void btnUserFeedbackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUserFeedbackActionPerformed
        // TODO add your handling code here:
        boolean rate;
        if(rdHailong.isSelected())
        rate = true;
        else
        rate = false;
        String comment = txtRate.getText();
        String err = "";
        if(!rdHailong.isSelected() && !rdKhonghailong.isSelected()){
            err += "\n- Your selection empty.";
        }
        if(comment.length()==0){
            err += "Your rating empty.";
        }

        if(err.length()==0){
            Usersfeedback fb = new Usersfeedback(0, rate, comment);
            boolean infb = new UsersfeedbackDAO().insertfeedback(fb);
            if(infb){
                JOptionPane.showMessageDialog(this, "Gửi feedback thành công !");
            }else{
                JOptionPane.showMessageDialog(this, "Gửi feedback thất bại !");
            }
        }else{
            JOptionPane.showMessageDialog(this, err);
        }
    }//GEN-LAST:event_btnUserFeedbackActionPerformed

