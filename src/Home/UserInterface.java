    public void load() {
        Connection con;
        CallableStatement ca = null;
        ResultSet rs = null;
        con = Ketnoi.open();
        try {
            ca = con.prepareCall("{call pCategory}");
            rs = ca.executeQuery();
            while (rs.next()) {
                Category pCa = new Category();
                pCa.setId(rs.getInt("id"));
                pCa.setName(rs.getString("name"));
                boxModel.addElement(pCa);
            }

        } catch (SQLException ex) {
            Logger.getLogger(categoryDao.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            Ketnoi.closeAll(con, ca, rs);
        }
    }

