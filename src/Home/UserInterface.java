    private void LoadProduct() {
        _Product.setRowCount(0);
        List<product> GetAllCategory = new productDAO().GetAllCategory();
        for (product _pDAO : GetAllCategory) {
            String tinhTrang;
            if (_pDAO.isStatus() == true) {
                tinhTrang = "100%";
            } else {
                tinhTrang = "99%";
            }
            Object[] ob = {_pDAO.getId(), _pDAO.getName(), _pDAO.getPrice(), _pDAO.getSalePrice(), _pDAO.getGuarantee(), tinhTrang};
            _Product.addRow(ob);
        }
    }
