    private void jLabel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel8MouseClicked
        _Product.setRowCount(0);
        String ten = txtNameH.getText();
        String sql = "select top 1 p.*,c.name as 'CategoryName' from Product p inner join Category c on p.category_id = c.id  where ";
         if (ten.length() == 0) {
            ten = "N'%'";
        } else {
            ten = "N'%" + ten + "%'";
        }
        sql += "p.name like " + ten;
        List<product> search = new productDAO().search_product(sql);
        for (product p : search) {
            String st;
            if (p.isStatus() == true) {
                st = "Mới";
            } else {
                st = "cũ";
            }
            Object[] dt = {p.getId(), p.getName(), p.getPrice(), p.getSalePrice(), p.getGuarantee(), st};
            _Product.addRow(dt);
        }
    }//GEN-LAST:event_jLabel8MouseClicked

