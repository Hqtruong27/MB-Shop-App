    private void LoadProduct() {
        _Product.setRowCount(0);
        List<product> GetAllCategory = new productDAO().GetAllCategory();
        for (product _pDAO : GetAllCategory) {
            String st;
            if (_pDAO.isStatus() == true) {
                st = "Mới";
            } else {
                st = "cũ";
            }
            Object[] ob = {_pDAO.getId(), _pDAO.getName(), _pDAO.getPrice(), _pDAO.getSalePrice(), _pDAO.getImages(), _pDAO.getContent(), _pDAO.getNameCategory(), _pDAO.getGuarantee(), st};
            _Product.addRow(ob);
        }
    }

